DATA SEGMENT
        NUM1 DB 99H
        NUM2 DB 98H
        NUM3 DB ?
        CARRY DB 0
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AH,00H; CLEAR UPPER NIBBLE FOR ADDITION
	MOV AL,NUM1
	MOV BL,NUM2
        ADD AL,BL
        JNC NEXT
            INC CARRY
        NEXT:
        MOV AL,NUM1
        ADD AL,BL
        DAA
        MOV NUM3,AL
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
